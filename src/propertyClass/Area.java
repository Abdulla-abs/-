package propertyClass;

public class Area {
	String areaid;
	String name;
	int havingall;
	int building;
	int villa;
	public String getAreaid() {
		return areaid;
	}
	public void setAreaid(String areaid) {
		this.areaid = areaid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHavingall() {
		return havingall;
	}
	public void setHavingall(int havingall) {
		this.havingall = havingall;
	}
	public int getBuilding() {
		return building;
	}
	public void setBuilding(int building) {
		this.building = building;
	}
	public int getVilla() {
		return villa;
	}
	public void setVilla(int villa) {
		this.villa = villa;
	}
}
